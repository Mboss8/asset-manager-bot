export type Role = "OWNER" | "ADMIN" | "PM" | "FINANCE" | "MEMBER" | "GUEST";

export const ROLE_LEVELS: Record<Role, number> = {
  OWNER: 100,
  ADMIN: 80,
  PM: 60,
  FINANCE: 55,
  MEMBER: 30,
  GUEST: 10,
};

export const VISIBLE_ROLES: Record<string, Role[]> = {
  ALL_USERS: ["OWNER", "ADMIN", "PM", "FINANCE", "MEMBER", "GUEST"],
  MEMBER_OR_ABOVE: ["OWNER", "ADMIN", "PM", "FINANCE", "MEMBER"],
  ADMIN_ONLY: ["OWNER", "ADMIN"],
  PM_OR_ADMIN: ["OWNER", "ADMIN", "PM"],
  FINANCE_OR_ADMIN: ["OWNER", "ADMIN", "FINANCE"],
  OWNER_ONLY: ["OWNER"],
};

export function hasPermission(role: Role, allowedRoles: Role[]): boolean {
  return allowedRoles.includes(role);
}

export function canAccess(role: Role, policyKey: string): boolean {
  const allowed = VISIBLE_ROLES[policyKey];
  if (!allowed) return false;
  return hasPermission(role, allowed);
}

